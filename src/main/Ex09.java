package main;

public class Ex09 {

	public static void main(String[] args) {

		byte b = 10;
		int i = b;
		System.out.println("i: " + i);
		
		float f = 10.0f;
		double d = f;
		System.out.println("d: " + d);
		
		int iNum = 1;
		float fNum = iNum;
		System.out.println("fNum: " + fNum);
		
		System.out.println("iNum + fNum: " + (iNum + fNum));
		
	
	}

}
